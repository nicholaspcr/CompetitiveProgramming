void solve(){	
  int n;cin>>n;
  int ans = 0;
  while(n > 1){
    if(n%6 == 2 || n%6 ==4){
      cout<<-1<<endl;
      return;
    }
    if(n%6)
      n*=2;
    else
      n/=6;
    ans++;
  }
  cout<<ans<<endl;
}

